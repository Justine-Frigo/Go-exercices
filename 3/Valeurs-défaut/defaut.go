package main

import "fmt"

func main() {
    var i int
    var f32 float32
    var f64 float64
    var b bool
    var s string

    fmt.Println("Valeur par défaut de int :", i)
    fmt.Println("Valeur par défaut de float32 :", f32)
    fmt.Println("Valeur par défaut de float64 :", f64)
    fmt.Println("Valeur par défaut de bool :", b)
    fmt.Println("Valeur par défaut de string :", s)
}
